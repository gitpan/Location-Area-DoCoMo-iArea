package Location::Area::DoCoMo::iArea::Region;

use strict;

sub seek
{
  shift;
  seek DATA,0,0;
  my ($aid,$said) = @_;
  my ($region,$pref);
  while (my $line = <DATA>)
  {
    my @scan = $line =~ /^$aid,$said,([^,]+),([^,]+),/;
    if (@scan)
    {
      ($region,$pref) = @scan;
      last;
    }
  }
  return ($region,$pref);
}

